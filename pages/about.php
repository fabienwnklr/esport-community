                    <h4 class="h4 text-primary">A propos de ESQ</h4>
                    <p style="text-align:justify;">Née en août 2014, ESQ est à la base une simple idée de site internet permettant de créer un tournoi en quelques clics de souris pour générer de la vue à une structure. C'est lors de la réalisation de celui-ci que le projet pris une tournure tout à fait inédite. En effet, Geoffrey RODRIGUEZ et Marc ANDERLINI, les créateurs de ESQ, ont voulu dépasser toutes les idées actuelles sur le management de tournoi.
                        C’est alors qu’ils décidèrent d’ajouter une réelle plus-value à ce projet pour l’amener à la rentabilité dans un objectif de création d’entreprise dans le sport électronique, l’objectif étant de proposer des services innovants pour les utilisateurs pour ainsi promouvoir indirectement l’esport en France. L’objectif final est de proposer une plateforme complète pour joueurs esportifs.</p>
                    <h4 class="h4 text-primary">Made In France</h4>
                    <p style="text-align:justify;">Pour proposer un site internet de qualité, les développeurs travaillent, utilisent, développent, avec des outils qui sont pour la plupart, de création Française. A travers ce site, les développeurs mettent en avant leurs compétences au profit d’utilisateurs internationaux. L’entreprise DNG cultive alors l’image des produits Français à travers un simple site. C’est aussi également l’occasion de mettre en avant un produit de création française dans le monde du sport électronique.</p>
                    <h4 class="h4 text-primary">Premier système de ligues</h4>
                    <p style="text-align:justify;">Avec ce nouveau système de tournois, ESQ introduit le premier système de ligues, de classements d’équipes en France. L’objectif est d’établir un classement représentatif du véritable niveau des équipes à travers un classement qui pourra devenir une référence. Ce nouveau système de ligues arrive avec son lots de nouveautés puisque qu’un algorithme complexe permet de calculer les points avec un très grand nombre de variables.</p>
